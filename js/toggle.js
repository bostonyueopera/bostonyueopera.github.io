function openNav() {
  document.getElementById("blogSidenav").style.width = "285px";
  document.getElementById("blogSidenav").style.padding = "0 10px 0 25px";
  document.getElementById("blogSidenav").style.margin = "0 0 0 -48px";
}

function closeNav() {
  document.getElementById("blogSidenav").style.width = "0";
  document.getElementById("blogSidenav").style.padding = "0 0 0 0";
  document.getElementById("blogSidenav").style.margin = "0 0 0 0";
}
